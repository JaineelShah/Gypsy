package com.mpstme.gypsy2.models;

public class Hotel {
    String name;
    String email;
    String address;
    String image;
    String number;
    String cost;
    String rating;
    String identifier;

    public String getName(){
        return name;
    }

    public String getEmail(){
        return email;
    }

    public String getAddress(){
        return address;
    }

    public String getImage(){
        return image;
    }
    public String getCost(){
        return cost;
    }

    public String getRating(){
        return rating;
    }

    public String getIdentifier(){
        return identifier;
    }

    public String getNumber(){
        return number;
    }
}
